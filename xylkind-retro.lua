t=0
c=0
eggs_laid = false
NUM_EGGS = 8


keeper_player = {
  position = {
    x=0,
    y=0
  },
}

eggs = {  
  position = {
    x=0,
    y=0
  },
  hatched = false,
}




function lay_eggs()
  for egg_count = 1,8 do
    eggs[egg_count] = {
      position = {
        x=math.random(233),
        y=math.random(134)
      },
    }
  end
  eggs_laid = true
end


function draw_eggs()
  if not eggs_laid then
    lay_eggs()
  end

  for egg_count = 1, NUM_EGGS do  
    spr(64+egg_count % 4,
      eggs[egg_count].position.x,
      eggs[egg_count].position.y)
  end
end

function TIC()

  cls()
  
  draw_eggs()
  if btn(0) then keeper_player.position.y=keeper_player.position.y-1 end
  if btn(1) then keeper_player.position.y=keeper_player.position.y+1 end
  if btn(2) then keeper_player.position.x=keeper_player.position.x-1 end
  if btn(3) then keeper_player.position.x=keeper_player.position.x+1 end
  spr(t % 4,keeper_player.position.x,keeper_player.position.y)
  spr((t + 16) % 4,keeper_player.position.x + 8,keeper_player.position.y)
  spr((t + 32) % 4,keeper_player.position.x + 16,keeper_player.position.y)
  c=c+1
  
  if c % 30 == 0 then
    t=t+1
  end
end

